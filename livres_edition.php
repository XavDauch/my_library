        <h1>L'hygiène de l'assassin</h1>
        <img src="image/hygiene.jpg" alt="Hygiène de l'assassin">
        <p>Prétextat Tach, prix Nobel de littérature, n’a plus que deux mois à vivre. Des journalistes du monde entier sollicitent des interviews de l’écrivain que sa misanthropie tient reclus depuis des années. Quatre seulement vont le rencontrer, dont il se jouera selon une dialectique où la mauvaise foi et la logique se télescopent. La cinquième lui tiendra tête, il se prendra au jeu.
        Si ce roman est presque entièrement dialogué, c’est qu’aucune forme ne s’apparente autant à la torture. Les échanges, de simples interviews, virent peu à peu à l’interrogatoire, à un duel sans merci où se dessine alors un homme différent, en proie aux secrets les plus sombres.
        Dans ce premier roman d’une extraordinaire intensité, Amélie Nothomb manie la cruauté, le cynisme et l’ambiguïté avec un talent accompli.
        </p>
